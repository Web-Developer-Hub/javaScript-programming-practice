// find out factorial number of any number...useing while loop..
let i = 1;
let factorial = 1;
while (i <= 7) {
    factorial = factorial * i;
    i += 1;
}
console.log(factorial);


// find out factorial number of any number...useing while loop..
let x = 1;
let myFactorial = 1;
const loopValue = 6;

while (x <= loopValue) {
    myFactorial = myFactorial * x;
    x += 1;
}
