// something math using arrow function
const doMath = (number1, number2) => {
    return (number1 + 2) * (number2 + 2);
}
const result = doMath(5, 2)
console.log(result);

const doMath2 = (number1, number2) => {
    return (number1 + 2) / (number2 + 2);
}
const result2 = doMath2(53, 20)
console.log(result2);


const doMath3 = (number1, number2) => {
    return (number1 + 2) - (number2 + 2);
}
const result3 = doMath3(53, 45)
console.log(result3);


const doMath4 = (number1, number2) => {
    return (number1 + 2) + (number2 + 2);
}
const result4 = doMath4(23, 45)
console.log(result4);


const doMath5 = (number1, number2) => {
    return (number1 + 2) % (number2 + 2);
}
const result5 = doMath5(20, 2)
console.log(result5);